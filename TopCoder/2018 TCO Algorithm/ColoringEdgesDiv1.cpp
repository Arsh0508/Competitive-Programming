using vpii = vector<pair<int, int>> ;

vector<vpii> adj ;
vi color ;
vb visit ;

void init(int n)
  {
    adj.resize(n) ;
    color.resize((3*n)/2) ;
    visit.assign((3*n)/2, false) ;
  }

void dfs(int u, int e, int c)
  {
    if(visit[e]) return ;
    color[e]=c ;
    visit[e]=true ;
    for(auto v:adj[u]) dfs(v.fi, v.se, 1-c) ;
  }

class ColoringEdgesDiv1 {
    public:
    vector<int> findColoring(int n, vector<int> x, vector<int> y) {
        init(n) ;
        for(int i=0 ; i<x.size() ; i++)
          {
            adj[x[i]].pb(mp(y[i], i)) ;
            adj[y[i]].pb(mp(x[i], i)) ;
          }
        for(auto v:adj[0]) dfs(v.fi, v.se, 0) ;
        return color ;
    }
};

